    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int minPassword = getMinPassword();

        int passLength;

        try{
            passLength = Integer.parseInt(passwordLength.getText());
        }catch(Exception e){
            System.out.println("Not a number");
            passLength =-1;
        }
        
        
        if (passLength >= minPassword) {

            PassGen passgen = new PassGen(isCapitalReq.isSelected(),
                    isLowerReq.isSelected(),
                    isNumReq.isSelected(),
                    passLength
            );

            passwordDisplay.setText(passgen.getPassword());
        }else{
            passwordDisplay.setText("Length too short or not a Number");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

