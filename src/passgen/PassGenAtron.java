    private int getMinPassword() {
        int min = 0;
        if (isCapitalReq.isSelected()) {
            min++;
        }
        if (isLowerReq.isSelected()) {
            min++;
        }
        if (isNumReq.isSelected()) {
            min++;
        }
        return min;
    }
